def list_subdirectories(directory):
    """获取指定目录下的所有子文件夹名称"""
    try:
        # 列出所有文件和文件夹
        items = os.listdir(directory)
        # 过滤出子文件夹
        subdirectories = [item for item in items if os.path.isdir(os.path.join(directory, item))]
        return subdirectories
    except Exception as e:
        print(f"获取文件夹时出错: {e}")
        return []


